

public class Datatypes {
    public static void main(String[] args) {

        /*
            * This program demonstrates the use of different data types in Java.
            * Primitive data types include int, double, char, and boolean.
            * Non-primitive data types include String.
            * Each data type is used to store different kinds of values.
            * The program initializes variables of each type and prints their values to the console.
            * Author: Saikrishna Reddy
            * Date: December 2025
        */
        // Primitive data types
        int intValue = 42;
        double doubleValue = 3.14;
        char charValue = 'A';
        boolean booleanValue = true;

        // Displaying primitive data types
        System.out.println("Integer Value: " + intValue);
        System.out.println("Double Value: " + doubleValue);
        System.out.println("Character Value: " + charValue);
        System.out.println("Boolean Value: " + booleanValue);

        // Non-primitive data type (String)
        String stringValue = "Hello, Java!";
        System.out.println("String Value: " + stringValue);
    }
}   