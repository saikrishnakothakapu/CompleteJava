

public class ForLoop {
    public static void main(String[] args) {

        /*
            A for loop is used to repeat a block of code a specific number of times.
            It consists of three main parts:
            1. Initialization: Setting the starting point (e.g., int i = 0).
            2. Condition: The loop continues as long as this condition is true (e.g., i < 10).
            3. Increment/Decrement: Updating the loop variable (e.g., i++ to increase by 1).
        */

        // Example of a for loop that prints numbers from 1 to 5
        for (int i = 1; i <= 5; i++) {
            System.out.println(i);
        }
    }
}