
public class NestedLoop {

    public static void main(String[] args) {

        /* Nested Loop Example
         * This program uses a nested loop to print a 5x5 grid of asterisks.
        */


        for(int i=0; i<5;i++){
            for(int j=0; j<5; j++){
                System.out.print("* ");

            }
            System.out.println();
        }
    }
}