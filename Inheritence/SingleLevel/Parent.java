
/*
 * Parent class demonstrating single-level inheritance.
 */

public class Parent {

    /* 
    Method to display a message specific to the Parent class
    */

    public void display() {
        System.out.println("This is the Parent class.");
    }

    public int add(int a, int b) {
        return a + b;
    }

    public int subtract(int a, int b) {
        return a - b;
    }   


}