

public class IfElseCondition {

    public static void main(String[] args) {

        /*
         * This is an example of an if-else condition in Java.
         * The program checks if a number is positive or not.
         */

        int number = 10;

        // If-else condition to check if the number is positive
        if (number > 0) {
            System.out.println("The number is positive.");
        } else {
            System.out.println("The number is not positive.");
        }
    }
}