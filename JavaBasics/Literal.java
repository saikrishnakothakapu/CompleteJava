

public class Literal {
    public static void main(String[] args) {

        /*
            A literal is a fixed value that is directly represented in the source code.
            Examples of literals include:
            - Integer literals (e.g., 100)
            - Floating-point literals (e.g., 99.99)
            - Character literals (e.g., 'A')
            - String literals (e.g., "Hello, World!")
            - Boolean literals (e.g., true, false)
            
            why are literals used?
            Literals are used to represent constant values in the code. They provide a way to define
            constant values directly in the source code without needing to declare variables.

            Author: Saikrishna Reddy
            Date: December, 2025


        */
        // Integer literal
        int intLiteral = 100;
        System.out.println("Integer Literal: " + intLiteral);

        // Floating-point literal
        double floatLiteral = 99.99;
        System.out.println("Floating-point Literal: " + floatLiteral);

        // Character literal
        char charLiteral = 'A';
        System.out.println("Character Literal: " + charLiteral);

        // String literal
        String stringLiteral = "Hello, World!";
        System.out.println("String Literal: " + stringLiteral);

        // Boolean literal
        boolean boolLiteral = true;
        System.out.println("Boolean Literal: " + boolLiteral);
    }
}