

public class IfCondition {

    public static void main(String[] args) {

        /*
            * This is a simple demonstration of an if condition in Java.
            * The program checks if a number is positive and prints a message accordingly.
        */
       
        int number = 10;

        // Simple if condition
        if (number > 0) {
            System.out.println("The number is positive.");
        }

    }
}