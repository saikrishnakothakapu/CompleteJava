public class Variables {


    public static void main(String[] args) {

        /* Declare and initialize variables
        ** Variables are used to store data values
        ** We cannot use a variable before declaring it
        ** We cannot declare a variable more than once in the same scope
        ** Variable names are case-sensitive
        ** We cannot initialize a variable without deciding its type
        ** Common data types: int, double, char, String, boolean
            */
        int age = 26;
        double height = 5.4;
        char grade = 'A';
        String name = "Saikrishna Reddy";
        boolean isStudent = false;

        // Print variable values
        System.out.println("Name: " + name);
        System.out.println("Age: " + age);
        System.out.println("Height: " + height);
        System.out.println("Grade: " + grade);
        System.out.println("Is Student: " + isStudent);
    }
}